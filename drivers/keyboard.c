void print_letter(uint8_t scancode) {

    if(scancode == 42) // shift in
    {
        state_shift = 1;
        return;
    }
    else if (scancode == 170) //shift out
    {
        state_shift = 0;
        return;
    }

    char c = (state_shift) ? sc_upper[scancode] : sc_lower[scancode];

    if(c != 0)
        print_char(c); 
}

// keyboard_callback is basically the handler for the interrupt ... this is set by initialize_keyboard
void keyboard_callback(registers_t r)
{
    uint8_t scancode = read_port_byte(0x60);
    print_letter(scancode);
}

void initialize_keyboard()
{
    set_irq_handler(33, keyboard_callback);
}
